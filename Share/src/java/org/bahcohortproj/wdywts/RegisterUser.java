    /**
     * Handles the HTTP
     * <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP
     * <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        
        String _userName, _fName, _lName;
        _userName = request.getParameter("userName");
        _fName = request.getParameter("fName");
        _lName = request.getParameter("lName");
        
        UserDetail _user = new UserDetail();
        _user.setUserName(_userName);
        _user.setfName(_fName);
        _user.setlName(_lName);
        
        
        RegisterService registerService = new RegisterService();
        boolean userRegistered = registerService.registerUser(_user);
        
        if (userRegistered){
            //user was registered, auto dispatch to Login servlet to login the user
            RequestDispatcher dispatcher = request.getRequestDispatcher("login");
            dispatcher.forward(request, response);
            
        } else {
            response.sendRedirect("");
            return;
        }
        
        
    }

